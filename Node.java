
public class Node {

	private int x;
	private int y;
	
	public Node(int numX, int numY)
	{
		x = numX;
		y = numY;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
}
